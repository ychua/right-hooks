#!/usr/bin/env bash
# Tests for: inject-skill.sh (SubagentStart hook)
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
source "$SCRIPT_DIR/../helpers.sh"
setup_test_env

HOOK="$SCRIPT_DIR/../../../hooks/inject-skill.sh"

echo "inject-skill"

# Helper: create an isolated git repo with skills.json
# Overrides HOME to prevent finding real skill files in ~/.claude/skills/
new_repo() {
  REPO_DIR=$(mktemp -d)
  FAKE_HOME=$(mktemp -d)
  cd "$REPO_DIR"
  git init -q -b main
  git config user.email "test@test.com"
  git config user.name "Test"
  git commit --allow-empty -m "init" -q

  mkdir -p .right-hooks
  cat > .right-hooks/skills.json <<'SKILLS'
{
  "codeReview": {
    "skill": "/review",
    "provider": "gstack",
    "fallback": "Dispatch a code review subagent for PR #${PR_NUM}",
    "skillSignature": "Generated by /review"
  },
  "qa": {
    "skill": "/qa",
    "provider": "gstack",
    "fallback": "Dispatch a QA subagent for PR #${PR_NUM}",
    "skillSignature": "Generated by /qa"
  },
  "docConsistency": {
    "skill": "/document-release",
    "provider": "gstack",
    "fallback": "Post a documentation consistency comment on PR #${PR_NUM}",
    "skillSignature": "Documentation health:"
  }
}
SKILLS
}

# Helper: run hook with HOME overridden to prevent finding real skill files
run_isolated_hook() {
  local hook="$1"
  local json_input="$2"
  local stdout_file="$TEST_TMPDIR/stdout"
  local stderr_file="$TEST_TMPDIR/stderr"

  echo "$json_input" | HOME="$FAKE_HOME" RH_TEST=1 bash "$hook" >"$stdout_file" 2>"$stderr_file"
  LAST_EXIT=$?
  LAST_STDOUT="$stdout_file"
  LAST_STDERR="$stderr_file"
}

cleanup_repo() {
  rm -rf "$REPO_DIR" "$FAKE_HOME"
}

