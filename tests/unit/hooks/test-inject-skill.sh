# --- Test 1: Fast-exit for empty agent name ---
describe "fast-exits when agent_name is empty"
new_repo
run_isolated_hook "$HOOK" '{"agent_name": ""}'
assert_exit_code 0 "$LAST_EXIT"
STDOUT_CONTENT=$(cat "$LAST_STDOUT" 2>/dev/null)
if [ -z "$STDOUT_CONTENT" ]; then
  pass
else
  fail "Expected empty stdout for empty agent_name, got: $STDOUT_CONTENT"
fi
cleanup_repo

